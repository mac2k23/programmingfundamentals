#include <iostream>
using namespace std;
int main() 
{
    int x;
    x = 7 + 3 * 6 / 2 - 1;
    cout << "a) x = " << x << endl;
    x = 2 % 2 + 2 * 2 - 2 / 2;
    cout << "b) x = " << x << endl;
    x = 3 * 9 * (3 + 9 * 3 / 3);
    cout << "c) x = " << x << endl;
    x = 2 * 3 / 4 + 4 / 4 + 8 - 2 + 5 / 8;
    cout << "d) x = " << x << endl;
    x = 3 / 2 * 4 + 3 / 8 + 3;
    cout << "e) x = " << x << endl;

    return 0;
}

