#include<iostream>
using namespace std;
int main()
{
	if ('+' < '-')
    cout << "+-";
	cout << "-+" << endl;
	
	if (12 / 2 == 4 + 1)
    cout << "6 ";
	cout << "12 / 2 != 4 + 1" << endl;
	
	if ('*' >= '/')
    cout << "/";
	cout << "*";
	cout << endl;
	
	if ("C++" <= "++C")
    cout << "C++" << endl;
	cout << "C++" << endl;
	
	if ("low" <= "high")
    cout << "low" << endl;
	cout << "high" << endl;
	
	
}
